package pl.edu.wat.wcy.cop.app.client.domain;
// Represents import data object.

public class ImportDataObject {

    private String content;

    /**
     *
     */
    public ImportDataObject() {
        super();
    }

    /**
     * @param content
     */
    public ImportDataObject(String content) {
        super();
        this.content = content;
    }

    /**
     * @return the content
     */
    public String getContent() {
        return content;
    }

    /**
     * @param content
     *            the content to set
     */
    public void setContent(String content) {
        this.content = content;
    }

}
